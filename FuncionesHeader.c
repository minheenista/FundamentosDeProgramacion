#include "FuncionesHeader.h"

float suma(float a, float b){
    return a+b;
}

float resta (float a, float b){
    return a-b;
}

float multiplicacion(float a, float b){
    return a*b;
}

float division(float a, float b){
    return a/b;
}

float areaCirculo(float a){
    return pi*a*a;
}